package es.codegym.task.pro.task04.task0404;

/* 
Cuadrado relleno
*/

public class Solution {
    public static void main(String[] args) {
        int n = 0;

        while (n < 5)
        {
            int m = 0;

            while (m < 10)
            {
                System.out.print("Q");
                m++;
            }
            System.out.println();
            n++;
        }

    }
}